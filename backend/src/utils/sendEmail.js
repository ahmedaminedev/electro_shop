const nodemailer = require('nodemailer');

const sendEmail = async (options) => {
  // Configuration pour Mailtrap (pour le dev) ou un vrai SMTP
  const transporter = nodemailer.createTransport({
    host: process.env.SMTP_HOST || "smtp.mailtrap.io",
    port: process.env.SMTP_PORT || 2525,
    auth: {
      user: process.env.SMTP_EMAIL || "test_user",
      pass: process.env.SMTP_PASSWORD || "test_pass",
    },
  });

  const message = {
    from: `${process.env.FROM_NAME || 'Electro Shop'} <${process.env.FROM_EMAIL || 'noreply@electroshop.com'}>`,
    to: options.email,
    subject: options.subject,
    text: options.message,
    // html: options.html // Optionnel si vous voulez envoyer du HTML
  };

  const info = await transporter.sendMail(message);

  console.log('Message sent: %s', info.messageId);
};

module.exports = sendEmail;